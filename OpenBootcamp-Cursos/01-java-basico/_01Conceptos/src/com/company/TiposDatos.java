package com.company;

public class TiposDatos {
    public static void main(String[] args) {

        //Enteros
        byte number1 = 1; // 1 byte
        short number2 = 2; // 2 byte
        int number3 = 3; // 4 byte
        long number4 = 4L; // 8 byte

        // punto flotante
        float decimal1 = 4.9f;
        double decimal2 = 9.99d;

        //caracter
        char caracter1 = 'a';

        //booleanos
        boolean verdadero = true;
        boolean falso = false;

        // cadeans de texto
        String nombre = "Alan";
        String apellido = "Sastre";

        // Tipos envoltorio
        Integer numero = null;
        Long numero2 = 2L;

        System.out.println(number4);
    }
}
